// Helper functions for filtering and searching
export const getProductsByCategory = (category: string): Product[] => {
  return allProducts.filter((product) => {
    const productCategory = product.category.toLowerCase();
    const searchCategory = category.toLowerCase();

    // Try exact match first
    if (productCategory === searchCategory) {
      return true;
    }

    // Try partial match
    return (
      productCategory.includes(searchCategory) ||
      searchCategory.includes(productCategory)
    );
  });
};

export const getProductsBySKU = (sku: string): Product | undefined => {
  return allProducts.find((product) => product.sku === sku);
};

export const getFeaturedProducts = (): Product[] => {
  return allProducts.filter((product) => product.isFeatured);
};

export const getInStockProducts = (): Product[] => {
  return allProducts.filter(
    (product) => product.inStock && product.stockLevel > 0,
  );
};

export const searchProducts = (query: string): Product[] => {
  const searchTerm = query.toLowerCase();
  return allProducts.filter(
    (product) =>
      product.name.toLowerCase().includes(searchTerm) ||
      product.sku.toLowerCase().includes(searchTerm) ||
      product.category.toLowerCase().includes(searchTerm) ||
      product.brand.toLowerCase().includes(searchTerm) ||
      product.carModel.toLowerCase().includes(searchTerm) ||
      product.tags.some((tag) => tag.toLowerCase().includes(searchTerm)) ||
      product.compatibility.some((comp) =>
        comp.toLowerCase().includes(searchTerm),
      ),
  );
};
